// 题目：组合（LeetCode 77）
// 给定两个整数 n 和 k，返回范围 [1, n] 中所有可能的 k 个数的组合。
// 你可以按 任何顺序 返回答案。
//
// 示例 1：
//   输入：n = 4, k = 2
//   输出：[[1,2], [1,3], [1,4], [2,3], [2,4], [3,4]]
//
// 示例 2：
//   输入：n = 1, k = 1
//   输出：[[1]]

#include<iostream>
#include<vector>
#include<algorithm>

using namespace std;

class Solution {
public:
    // 主函数：接收 n 和 k，返回所有 k 个数的组合
    // @param n: 数字范围 1~n
    // @param k: 每个组合中数字的个数
    // @return: 所有可能的组合
    vector<vector<int>> combine(int n, int k) {
        // result: 用于存储所有组合的"大容器"，每个元素都是一个组合（vector<int>）
        vector<vector<int>> result;
        
        // current: 用于存储"当前正在构建"的一个组合
        // 它就像一个"临时篮子"，我们往里面放数字，凑够 k 个就记录一次
        vector<int> current;
        
        // 调用回溯函数，从数字 1 开始尝试选数
        // 参数说明：
        //   result  : 结果容器（传引用，递归中直接往里加结果）
        //   current : 当前正在构建的组合（传引用，递归中不断修改它）
        //   1       : 起始搜索位置（从数字 1 开始尝试）
        //   n       : 数字范围上限
        //   k       : 需要选择的数字个数
        backtrack(result, current, 1, n, k);
        
        // 返回所有组合
        return result;
    }

private:
    // 回溯函数（核心递归函数）
    // 思路：一棵"选择树"，从 start 到 n 逐个尝试每个数字
    //       选了当前数字后，递归去选下一个数字
    //       一旦凑够 k 个，就记录结果并返回
    //
    // @param result : 存放所有结果的大容器（传引用，这样递归中也能修改它）
    // @param current: 当前正在构建的组合（传引用）
    // @param start  : 本次可选的起始数字（防止重复，保证组合递增）
    // @param n      : 数字范围上限
    // @param k      : 需要选择的数字个数
    void backtrack(vector<vector<int>>& result, vector<int>& current, int start, int n, int k) {
        // ============================================================
        // 【终止条件】当前组合已经凑够了 k 个数字
        // ============================================================
        // 想象一下：你手里已经拿了 k 个数字，就不能再拿了
        // 此时应该把这个组合记录下来，然后"回头"去尝试其他可能
        if (current.size() == k) {
            // 把当前组合 current 的副本加入到结果集 result 中
            // 注意：这里必须传入 current 的副本（即 vector<int>(current) 的拷贝）
            // 不能传引用，因为 current 后续还会被修改
            result.push_back(current);
            
            // 返回上一级，尝试其他数字
            return;
        }

        // ============================================================
        // 【做选择】从 start 开始，逐个尝试每个数字
        // ============================================================
        // 为什么从 start 开始而不是从 1 开始？
        //   因为组合是无序的，[1,2] 和 [2,1] 是同一个组合
        //   所以我们规定：每次选的数字必须比之前大，这样就不会出现重复
        //
        // 举个例子：当前 current = [1]，那下一步只能从 2 开始选
        //          而不能回头选 1（否则会得到 [1,1] 或 [1,2] [2,1] 重复）
        for (int i = start; i <= n; i++) {
            // ----- ① 选择当前数字 i，放入"临时篮子" -----
            // 比如 current = [1]，选 i = 2，则 current 变成 [1,2]
            current.push_back(i);
            
            // ----- ② 递归：让"另一个自己"去选下一个数字 -----
            // 下一轮从 i + 1 开始选（不能选比 i 小的数，防止重复）
            // 比如选了 2，下一轮就只能从 3 开始选，不能回头选 1
            backtrack(result, current, i + 1, n, k);
            
            // ----- ③ 撤销选择（回溯的核心！）-----
            // 把刚才选的数字从"临时篮子"里拿出来
            // 然后 for 循环进入下一轮 i+1，尝试下一个数字
            //
            // 举例理解：
            //   current = [1,2] -> 记录结果 -> 返回
            //   current.pop_back() -> current = [1]
            //   for 循环 i++ -> i = 3 -> current = [1,3] -> ...
            current.pop_back();
        }
        // ============================================================
        // 回溯核心思想总结（口诀）：
        //   做选择   →  递归   →  撤销选择
        //   push_back → 递归调用 → pop_back
        //   就像走迷宫：往前走 → 走到头 → 退回来换个路口再走
        // ============================================================
    }
};
